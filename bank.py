import datetime

class Клиент:
    def __init__(self, имя, паспорт):
        self.имя = имя
        self.паспорт = паспорт
        self.счета = []
        self.кредиты = []
        self.депозиты = []

    def открыть_счёт(self, валюта, начальный_баланс=0):
        счёт = Счёт(self, валюта, начальный_баланс)
        self.счета.append(счёт)
        return счёт

    def подать_заявку_на_кредит(self, сумма, ставка, срок_месяцев):
        кредит = Кредит(self, сумма, ставка, срок_месяцев)
        self.кредиты.append(кредит)
        return кредит

    def открыть_депозит(self, сумма, ставка, срок_месяцев):
        депозит = Депозит(self, сумма, ставка, срок_месяцев)
        self.депозиты.append(депозит)
        return депозит


class Счёт:
    def __init__(self, владелец, валюта, баланс=0):
        self.владелец = владелец
        self.валюта = валюта
        self.баланс = баланс
        self.транзакции = []

    def внести(self, сумма):
        self.баланс += сумма
        self.транзакции.append(Транзакция('пополнение', сумма, datetime.datetime.now()))
        print(f"Внесено {сумма} {self.валюта}. Новый баланс: {self.баланс} {self.валюта}")
        return self.баланс

    def снять(self, сумма):
        if сумма > self.баланс:
            raise ValueError("Недостаточно средств")
        self.баланс -= сумма
        self.транзакции.append(Транзакция('снятие', сумма, datetime.datetime.now()))
        print(f"Снято {сумма} {self.валюта}. Остаток: {self.баланс} {self.валюта}")
        return self.баланс

    def закрыть(self):
        print(f"Счёт в валюте {self.валюта} закрыт. Итоговый баланс: {self.баланс} {self.валюта}")
        self.баланс = 0


class Кредит:
    def __init__(self, клиент, сумма, ставка, срок_месяцев):
        self.клиент = клиент
        self.сумма = сумма
        self.ставка = ставка
        self.срок_месяцев = срок_месяцев
        self.остаток = сумма
        self.статус = 'активен'

    def внести_платёж(self, сумма):
        if self.статус != 'активен':
            raise Exception("Кредит уже закрыт или просрочен")
        self.остаток -= сумма
        print(f"Внесён платёж по кредиту: {сумма}. Остаток долга: {self.остаток}")
        if self.остаток <= 0:
            self.статус = 'закрыт'
            print("Кредит полностью погашен!")
        return self.остаток

    def ежемесячный_платёж(self):
        # Формула аннуитетного платежа
        месячная_ставка = self.ставка / 100 / 12
        платёж = self.сумма * (месячная_ставка / (1 - (1 + месячная_ставка) ** (-self.срок_месяцев)))
        print(f"Ежемесячный аннуитетный платёж: {платёж:.2f}")
        return платёж


class Депозит:
    def __init__(self, клиент, сумма, ставка, срок_месяцев):
        self.клиент = клиент
        self.сумма = сумма
        self.ставка = ставка
        self.срок_месяцев = срок_месяцев
        self.статус = 'активен'

    def закрыть(self):
        проценты = self.сумма * self.ставка / 100 * self.срок_месяцев / 12
        итого = self.сумма + проценты
        self.статус = 'закрыт'
        print(f" Депозит закрыт. Начислено процентов: {проценты:.2f}. Итого к выдаче: {итого:.2f}")
        return итого


class Транзакция:
    def __init__(self, тип, сумма, дата):
        self.тип = тип
        self.сумма = сумма
        self.дата = дата


